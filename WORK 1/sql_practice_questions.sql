-- Q39. Find the top 3 revenue-contributing products within each category
--      (use dense_rank window function).
-- ANSWER:
WITH product_rev AS (
    SELECT
        cat.name                         AS category,
        p.name                           AS product_name,
        SUM(oi.quantity * oi.unit_price) AS revenue,
        DENSE_RANK() OVER (
            PARTITION BY cat.category_id
            ORDER BY SUM(oi.quantity * oi.unit_price) DESC
        ) AS dr
    FROM products p
    JOIN categories cat  ON cat.category_id = p.category_id
    JOIN order_items oi  ON oi.product_id   = p.product_id
    JOIN orders o        ON o.order_id      = oi.order_id
    WHERE o.status NOT IN ('cancelled','returned')
    GROUP BY cat.category_id, category, p.product_id, product_name
)
SELECT category, product_name, revenue, dr AS rank_in_category
FROM product_rev
WHERE dr <= 3
ORDER BY category, dr;

