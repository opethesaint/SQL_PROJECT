-- Q49. Create a stored procedure that generates a customer statement:
--      given a customer_id, list all their orders with totals.
-- ANSWER:
DELIMITER $$
CREATE PROCEDURE GetCustomerStatement(IN p_customer_id INT)
BEGIN
    SELECT
        o.order_id,
        o.order_date,
        o.status,
        COUNT(oi.item_id)              AS line_items,
        SUM(oi.quantity * oi.unit_price) AS subtotal,
        o.shipping_fee,
        o.discount,
        SUM(oi.quantity * oi.unit_price) + o.shipping_fee - o.discount AS total,
        py.method                      AS payment_method,
        py.status                      AS payment_status
    FROM orders o
    JOIN order_items oi ON oi.order_id = o.order_id
    LEFT JOIN payments py ON py.order_id = o.order_id
    WHERE o.customer_id = p_customer_id
    GROUP BY o.order_id, o.order_date, o.status,
             o.shipping_fee, o.discount, py.method, py.status
    ORDER BY o.order_date DESC;
END$$
DELIMITER ;
-- Usage: CALL GetCustomerStatement(1);

