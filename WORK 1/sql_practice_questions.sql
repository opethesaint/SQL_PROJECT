-- Q41. Create a stored procedure that accepts a category name and returns
--      the total revenue, total units sold, and average product rating.
-- ANSWER:
DELIMITER $$
CREATE PROCEDURE GetCategoryStats(IN p_category VARCHAR(100))
BEGIN
    SELECT
        cat.name                             AS category,
        SUM(oi.quantity * oi.unit_price)     AS total_revenue,
        SUM(oi.quantity)                     AS total_units_sold,
        ROUND(AVG(r.rating), 2)              AS avg_rating
    FROM categories cat
    JOIN products p     ON p.category_id    = cat.category_id
    LEFT JOIN order_items oi ON oi.product_id = p.product_id
    LEFT JOIN orders o       ON o.order_id    = oi.order_id
                             AND o.status NOT IN ('cancelled','returned')
    LEFT JOIN reviews r      ON r.product_id  = p.product_id
    WHERE cat.name = p_category
    GROUP BY cat.name;
END$$
DELIMITER ;
-- Usage: CALL GetCategoryStats('Electronics');

