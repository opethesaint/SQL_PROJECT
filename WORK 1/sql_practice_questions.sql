-- Q48. Write a query to calculate a 3-month rolling average of revenue.
-- ANSWER:
WITH monthly_rev AS (
    SELECT
        DATE_FORMAT(o.order_date,'%Y-%m')    AS month,
        SUM(oi.quantity * oi.unit_price)     AS revenue
    FROM orders o
    JOIN order_items oi ON oi.order_id = o.order_id
    WHERE o.status NOT IN ('cancelled','returned')
    GROUP BY month
)
SELECT
    month,
    revenue,
    ROUND(
        AVG(revenue) OVER (
            ORDER BY month
            ROWS BETWEEN 2 PRECEDING AND CURRENT ROW
        ), 2
    ) AS rolling_3m_avg
FROM monthly_rev
ORDER BY month;

