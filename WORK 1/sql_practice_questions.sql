-- Q37. Find the cohort retention: of customers who first ordered in 2022,
--      what percentage returned to order in 2023?
-- ANSWER:
WITH cohort_2022 AS (
    SELECT DISTINCT customer_id
    FROM orders
    WHERE YEAR(order_date) = 2022
),
returned_2023 AS (
    SELECT DISTINCT o.customer_id
    FROM orders o
    JOIN cohort_2022 c ON c.customer_id = o.customer_id
    WHERE YEAR(o.order_date) = 2023
)
SELECT
    COUNT(DISTINCT cohort_2022.customer_id) AS cohort_size,
    COUNT(DISTINCT returned_2023.customer_id) AS returned_in_2023,
    ROUND(
        COUNT(DISTINCT returned_2023.customer_id) * 100.0
        / COUNT(DISTINCT cohort_2022.customer_id), 2
    ) AS retention_rate_pct
FROM cohort_2022
LEFT JOIN returned_2023 ON returned_2023.customer_id = cohort_2022.customer_id;

