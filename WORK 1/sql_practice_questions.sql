-- Q40. Build a full customer RFM (Recency, Frequency, Monetary) report.
--      R = days since last order, F = total orders, M = total spend.
--      Score each dimension 1-4 (4 = best) and compute RFM total score.
-- ANSWER:
WITH rfm_raw AS (
    SELECT
        c.customer_id,
        CONCAT(c.first_name,' ',c.last_name)  AS customer_name,
        DATEDIFF(CURDATE(), MAX(o.order_date)) AS recency_days,
        COUNT(DISTINCT o.order_id)             AS frequency,
        SUM(oi.quantity * oi.unit_price)       AS monetary
    FROM customers c
    JOIN orders o       ON o.customer_id = c.customer_id
    JOIN order_items oi ON oi.order_id   = o.order_id
    WHERE o.status NOT IN ('cancelled','returned')
    GROUP BY c.customer_id, customer_name
),
rfm_scored AS (
    SELECT *,
        NTILE(4) OVER (ORDER BY recency_days DESC)  AS r_score,  -- lower days = higher score
        NTILE(4) OVER (ORDER BY frequency)          AS f_score,
        NTILE(4) OVER (ORDER BY monetary)           AS m_score
    FROM rfm_raw
)
SELECT customer_name,
       recency_days,
       frequency,
       monetary,
       r_score, f_score, m_score,
       (r_score + f_score + m_score) AS rfm_total,
       CASE
           WHEN (r_score + f_score + m_score) >= 10 THEN 'Champion'
           WHEN (r_score + f_score + m_score) >= 7  THEN 'Loyal'
           WHEN (r_score + f_score + m_score) >= 5  THEN 'Potential'
           ELSE 'At Risk'
       END AS customer_segment
FROM rfm_scored
ORDER BY rfm_total DESC;


