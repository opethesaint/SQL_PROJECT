-- Q44. Using a recursive CTE, list all category hierarchy paths
--      (e.g. Electronics > Phones & Tablets).
-- ANSWER:
WITH RECURSIVE cat_tree AS (
    SELECT category_id, name, parent_id,
           CAST(name AS CHAR(500)) AS path
    FROM categories
    WHERE parent_id IS NULL
    UNION ALL
    SELECT c.category_id, c.name, c.parent_id,
           CONCAT(ct.path, ' > ', c.name)
    FROM categories c
    JOIN cat_tree ct ON ct.category_id = c.parent_id
)
SELECT category_id, name, path
FROM cat_tree
ORDER BY path;

