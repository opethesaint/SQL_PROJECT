-- Q35. Determine stock risk: products where stock_qty is below
--      the average monthly sales volume (risk of stockout).
-- ANSWER:
WITH sales_rate AS (
    SELECT
        p.product_id,
        p.name,
        p.stock_qty,
        SUM(oi.quantity) AS total_sold,
        COUNT(DISTINCT DATE_FORMAT(o.order_date,'%Y-%m')) AS months_active,
        ROUND(SUM(oi.quantity) /
              COUNT(DISTINCT DATE_FORMAT(o.order_date,'%Y-%m')), 1) AS avg_monthly_sales
    FROM products p
    JOIN order_items oi ON oi.product_id = p.product_id
    JOIN orders o       ON o.order_id    = oi.order_id
    WHERE o.status NOT IN ('cancelled','returned')
    GROUP BY p.product_id, p.name, p.stock_qty
)
SELECT product_id, name, stock_qty, avg_monthly_sales,
       ROUND(stock_qty / avg_monthly_sales, 1) AS months_of_stock_remaining
FROM sales_rate
WHERE stock_qty < avg_monthly_sales
ORDER BY months_of_stock_remaining;

