-- Q32. Calculate Customer Lifetime Value (CLV) = total revenue per customer,
--      and segment them as: 'VIP' (>2M), 'Regular' (500k–2M), 'Occasional' (<500k).
-- ANSWER:
WITH clv AS (
    SELECT
        c.customer_id,
        CONCAT(c.first_name,' ',c.last_name) AS customer_name,
        SUM(oi.quantity * oi.unit_price)     AS lifetime_value
    FROM customers c
    JOIN orders o      ON o.customer_id = c.customer_id
    JOIN order_items oi ON oi.order_id  = o.order_id
    WHERE o.status NOT IN ('cancelled','returned')
    GROUP BY c.customer_id, customer_name
)
SELECT customer_name,
       lifetime_value,
       CASE
           WHEN lifetime_value > 2000000  THEN 'VIP'
           WHEN lifetime_value >= 500000  THEN 'Regular'
           ELSE 'Occasional'
       END AS segment
FROM clv
ORDER BY lifetime_value DESC;

