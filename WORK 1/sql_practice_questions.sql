-- Q47. For each customer, compute their share of total platform revenue
--      as a percentage, and rank them.
-- ANSWER:
WITH total AS (
    SELECT SUM(oi.quantity * oi.unit_price) AS grand_total
    FROM order_items oi
    JOIN orders o ON o.order_id = oi.order_id
    WHERE o.status NOT IN ('cancelled','returned')
),
cust_rev AS (
    SELECT
        c.customer_id,
        CONCAT(c.first_name,' ',c.last_name)  AS customer_name,
        SUM(oi.quantity * oi.unit_price)       AS revenue
    FROM customers c
    JOIN orders o      ON o.customer_id = c.customer_id
    JOIN order_items oi ON oi.order_id  = o.order_id
    WHERE o.status NOT IN ('cancelled','returned')
    GROUP BY c.customer_id, customer_name
)
SELECT customer_name,
       revenue,
       ROUND(revenue / t.grand_total * 100, 2) AS revenue_share_pct,
       RANK() OVER (ORDER BY revenue DESC)      AS revenue_rank
FROM cust_rev, total t
ORDER BY revenue DESC
LIMIT 20;

