-- Q38. For each customer, calculate the gap (in days) between consecutive orders
--      using window functions, and find the average gap per customer.
-- ANSWER:
WITH order_gaps AS (
    SELECT
        customer_id,
        order_date,
        LAG(order_date) OVER (PARTITION BY customer_id ORDER BY order_date) AS prev_order_date,
        DATEDIFF(order_date,
            LAG(order_date) OVER (PARTITION BY customer_id ORDER BY order_date)
        ) AS days_since_last_order
    FROM orders
)
SELECT
    c.customer_id,
    CONCAT(c.first_name,' ',c.last_name) AS customer_name,
    ROUND(AVG(og.days_since_last_order), 1) AS avg_days_between_orders
FROM customers c
JOIN order_gaps og ON og.customer_id = c.customer_id
WHERE og.days_since_last_order IS NOT NULL
GROUP BY c.customer_id, customer_name
ORDER BY avg_days_between_orders;

