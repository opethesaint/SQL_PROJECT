-- Q46. Write a query to flag orders where the payment amount does NOT match
--      the expected order total (data integrity check).
-- ANSWER:
SELECT
    o.order_id,
    o.status,
    SUM(oi.quantity * oi.unit_price) + o.shipping_fee - o.discount AS expected_amount,
    p.amount                                                          AS paid_amount,
    ABS(
        SUM(oi.quantity * oi.unit_price) + o.shipping_fee - o.discount
        - p.amount
    ) AS discrepancy
FROM orders o
JOIN order_items oi ON oi.order_id  = o.order_id
JOIN payments p     ON p.order_id   = o.order_id
GROUP BY o.order_id, o.status, o.shipping_fee, o.discount, p.amount
HAVING discrepancy > 0.01
ORDER BY discrepancy DESC;

