-- Q50. CAPSTONE — Build a full executive dashboard query:
--      Show KPIs: total revenue, total orders, avg order value,
--      total customers, total products sold, return rate, and
--      top category by revenue — ALL in a single result set.
-- ANSWER:
SELECT
    FORMAT(SUM(oi.quantity * oi.unit_price), 2)     AS total_revenue,
    COUNT(DISTINCT o.order_id)                       AS total_orders,
    FORMAT(SUM(oi.quantity * oi.unit_price)
           / COUNT(DISTINCT o.order_id), 2)          AS avg_order_value,
    (SELECT COUNT(*) FROM customers)                 AS total_customers,
    SUM(oi.quantity)                                 AS total_units_sold,
    ROUND(
        (SELECT COUNT(*) FROM orders WHERE status = 'returned')
        / COUNT(DISTINCT o.order_id) * 100, 2
    )                                                AS return_rate_pct,
    (
        SELECT cat2.name
        FROM categories cat2
        JOIN products p2        ON p2.category_id  = cat2.category_id
        JOIN order_items oi2    ON oi2.product_id  = p2.product_id
        JOIN orders o2          ON o2.order_id     = oi2.order_id
        WHERE o2.status NOT IN ('cancelled','returned')
        GROUP BY cat2.category_id
        ORDER BY SUM(oi2.quantity * oi2.unit_price) DESC
        LIMIT 1
    )                                                AS top_category
FROM orders o
JOIN order_items oi ON oi.order_id = o.order_id
WHERE o.status NOT IN ('cancelled','returned');


